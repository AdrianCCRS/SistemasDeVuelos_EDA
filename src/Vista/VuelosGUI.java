    /**
     * Calcula según el precio mínimo el camino más corto de un vuelo específico entre dos ciudades, ya sea directo o no (escalas).
     * @param evt 
     */
    private void vuelo_esp_precio_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vuelo_esp_precio_btnActionPerformed
        int idx1 = vuelo_esp_cb1.getSelectedIndex();
        int idx2 = vuelo_esp_cb2.getSelectedIndex();
        String salida = "La ruta de vuelos más barata desde " + mapCiudades[idx1] + " a " + mapCiudades[idx2] + " es: \n";
        if(idx1 == idx2){
            vuelo_esp_salida_txt.setText("Seleccione un vuelo válido");
        }
        else{
            salida += grafoVuelos.dijkstra(grafoVuelos.getMatrizPesos(),idx1, mapCiudades, idx2 ) + "$";
            vuelo_esp_salida_txt.setText(salida);
        }
    }//GEN-LAST:event_vuelo_esp_precio_btnActionPerformed

