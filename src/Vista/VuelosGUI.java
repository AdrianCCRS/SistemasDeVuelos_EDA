    /**
     * Crea una oferta de vuelo para el usuario (abarata el peso de la arista de ese vuelo, por lo que la oferta debe hacerse sobre un vuelo directo
     * @param evt
     */
    private void oferta_aplicar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oferta_aplicar_btnActionPerformed
        count_novedades_ofertas++;
        int idx1 = oferta_cb_1.getSelectedIndex();
        int idx2 = 0;
        
        //Calculando cual es la otra ciudad del descuento. Debido a la resticción de opciones del segundo combo box
        String ciudad2 = (String) oferta_cb_2.getSelectedItem();
        for (int i = 0; i < mapCiudades.length; i++) {
            if(mapCiudades[i].equals(ciudad2)){
                 idx2 = i;
            }
        }
        
        //Calculando el descuento
        float porcentajeNum = (int) oferta_porcentaje_num.getValue();
        float precioRegular = grafoVuelos.getMatrizPesos()[idx1][idx2];
        float descuento = (porcentajeNum/100)*precioRegular;
        float precioDescuento = precioRegular - descuento;
        
        grafoVuelos.metodoOfertas(idx1, idx2, precioDescuento);
        updateTabla(); //Actualiza la tabla
        
        //Actializa la vista de novedades
        novedades_icon.setVisible(true);
         novedades_ofertas[count_novedades_ofertas] =  "Descuento en los vuelos " + mapCiudades[idx1] + "-" + mapCiudades[idx2] + " del "  + (int)porcentajeNum + "%\n";
         String str ="";
        for (String novedades_oferta : novedades_ofertas) {
                if( novedades_oferta != null){
                str += novedades_oferta;
                }
            }          
          novedades_txt_area.setText(str);
    }//GEN-LAST:event_oferta_aplicar_btnActionPerformed

    /**
     * Elimina una oferta específica y elimina esa oferta del panel de novedades
     * @param evt 
     */
    private void oferta_eliminar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oferta_eliminar_btnActionPerformed
        int idx1 = oferta_cb_1.getSelectedIndex();
        int idx2 = 0;
        
        //Calculando cual es la otra ciudad del descuento
        String ciudad2 = (String) oferta_cb_2.getSelectedItem();
        for (int i = 0; i < mapCiudades.length; i++) {
            if(mapCiudades[i].equals(ciudad2)){
                 idx2 = i;
            }
        }
        
        grafoVuelos.anularOferta(idx1, idx2); //Anula la oferta
        updateTabla(); //Actualiza la tabla
        
        //Quitando la oferta del text area de novedades
        String str ="";
        for (int i = 0; i < novedades_ofertas.length; i++) {
            if( novedades_ofertas[i] != null){
                if( novedades_ofertas[i].contains(mapCiudades[idx1]) && novedades_ofertas[i].contains(mapCiudades[idx2])){
                 novedades_ofertas [i] = null;
                }
            }
        }        
        for (String novedades_oferta : novedades_ofertas) {
            if( novedades_oferta != null){
            str += novedades_oferta;
            }
        }        
       novedades_txt_area.setText(str);
    }//GEN-LAST:event_oferta_eliminar_btnActionPerformed

