        int idx1 = vuelo_esp_cb1.getSelectedIndex();
        int idx2 = vuelo_esp_cb2.getSelectedIndex();
        String salida = "La ruta de vuelos más rápida desde " + mapCiudades[idx1] + " a " + mapCiudades[idx2] + " es: \n";
